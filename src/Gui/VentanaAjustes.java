    public static int obtenerTarifas() {
        String Tarifa = (String) baseDatos.obtenerDatosEnLineaEspecifica((String) baseDatos.obtenerTodosLosDatos("bdConfiguraciones.txt").get(0)).get(0);
        int tarifaInt = Integer.parseInt(Tarifa);
        tarifa = tarifaInt + "|";
        etiqTarifa.setText(tarifaInt + " RD$");
        etiqTarifaPesados.setText((tarifaInt * 2) + " RD$");
        return tarifaInt;
    }

    private void editarTarifa(String tarifaNueva) {
        baseDatos.editarArchivo("bdConfiguraciones.txt", tarifa, tarifaNueva + "|");
    }

    private void obtenerRegistrosParaCombo(String bdDeseada) {

        ArrayList<String> listaTodosLosDatos = baseDatos.obtenerTodosLosDatos(bdDeseada);
        ArrayList<String> listaDatosEspecifico;

        String nombreUsuario = "";

        for (int i = 0; i < listaTodosLosDatos.size(); i++) {

            listaDatosEspecifico = baseDatos.obtenerDatosEnLineaEspecifica(listaTodosLosDatos.get(i));

            for (int j = 0; j < listaDatosEspecifico.size(); j++) {
                if (j == 0) {
                    nombreUsuario = listaDatosEspecifico.get(j);
                    modelo.addElement(nombreUsuario);
                }
            }

        }
    }

    private void habilitarCampos(boolean estado) {
        txtUsuario.setEnabled(estado);
        txtNombre.setEnabled(estado);
        txtContrasena.setEnabled(estado);
        comboCargo.setEnabled(estado);
        btnEjecutar.setEnabled(estado);
    }

    private void borrarCampos() {
        txtNombre.setText("");
        txtContrasena.setText("");
        txtUsuario.setText("");
        comboCargo.setSelectedIndex(0);
    }
