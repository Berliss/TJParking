    private void btnEjecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEjecutarActionPerformed
        if (!(txtUsuario.getText().isEmpty() || txtNombre.getText().isEmpty() || txtContrasena.getText().isEmpty())) {

            if (comboAccion.getSelectedIndex() == 0) {
                String Linea = txtUsuario.getText() + "|" + new String(txtContrasena.getPassword()) + "|" + comboCargo.getSelectedItem().toString() + "|" + txtNombre.getText() + "|";
                baseDatos.insertarDatos(Linea, "bdUsuario.txt", null);
                borrarCampos();

            } else if (comboAccion.getSelectedIndex() == 1) {
                String lineaNueva = txtUsuario.getText() + "|" + new String(txtContrasena.getPassword()) + "|" + comboCargo.getSelectedItem().toString() + "|" + txtNombre.getText() + "|";
                baseDatos.editarArchivo("bdUsuario.txt", linea, lineaNueva);
                comboAccion.setSelectedIndex(0);
                borrarCampos();
                txtUsuario.setEnabled(true);
            } else if (comboAccion.getSelectedIndex() == 2) {
                String linea = txtUsuario.getText() + "|" + new String(txtContrasena.getPassword()) + "|" + comboCargo.getSelectedItem().toString() + "|" + txtNombre.getText() + "|";
                baseDatos.eliminarLineaDeArchivo("bdUsuario.txt", linea);
                comboAccion.setSelectedIndex(0);
                borrarCampos();
                txtUsuario.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btnEjecutarActionPerformed


