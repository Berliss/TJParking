    private void insercionDatosDeClientes() {
        String lineaDeDatos = "";
        if (focusCampoVacio() == false) {
            for (JTextField campoActual : listaCampoDeTexto) {
                lineaDeDatos += campoActual.getText() + "|";
            }
            if (!validarCedulaCliente(lineaDeDatos)) {
                baseDatos.insertarDatos(lineaDeDatos, "bdClientes.txt", null);
                borrarCamposTexto();
                EditarPuedeFuncionar = false;
            }

        }
    }

