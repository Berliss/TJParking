    private boolean validarCedulaCliente(String lineaActual) {

        ArrayList<String> listaTodosLosClientes = baseDatos.obtenerTodosLosDatos("bdClientes.txt");
        ArrayList<String> listaClienteEspecifico;
        ArrayList<String> lista = baseDatos.obtenerDatosEnLineaEspecifica(lineaActual);

        String cedula = lista.get(1);

        for (int i = 0; i < listaTodosLosClientes.size(); i++) {

            listaClienteEspecifico = baseDatos.obtenerDatosEnLineaEspecifica(listaTodosLosClientes.get(i));

            String cedulaObtenida = listaClienteEspecifico.get(1);

            if (cedula.equals(cedulaObtenida)) {
                etiquetaDeError.setText("DOS CLIENTES NO PUEDEN TENER LA MISMA CEDULA");
                return true;
            }
        }
        return false;
    }

