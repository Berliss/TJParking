    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (bdSeleccionada.equalsIgnoreCase("ventas")) {
            etiqMonto.setText(null);
            String fecha = metodosGenerales.DateAString(jDateChooser1.getDate());
            int fechaInt = Integer.parseInt(fecha.replaceAll(Pattern.quote("/"), ""));

            borrarTabla(bdSeleccionada);
            obtenerRegistosParaTablaVentas(fechaInt);

        } else {

            String fechaDesde = metodosGenerales.DateAString(jDateChooser2.getDate());
            String fechaHasta = metodosGenerales.DateAString(jDateChooser1.getDate());

            int fechaDesdeInt = Integer.parseInt(fechaDesde.replaceAll(Pattern.quote("/"), ""));
            int fechaHastaInt = Integer.parseInt(fechaHasta.replaceAll(Pattern.quote("/"), ""));

            System.out.println("" + fechaDesdeInt + "  " + fechaHastaInt);

            borrarTabla(bdSeleccionada);
            obtenerRegistrosParaLaTabla(bdSeleccionada, fechaDesdeInt, fechaHastaInt);
        }

        if (bdSeleccionada.equals("bdSalidas.txt")) {
            String valor;
            int valorint = 0;
            int numeroFilas = modeloRetirados.getRowCount();
            for (int i = 0; i < numeroFilas; i++) {
                valor = (String) modeloRetirados.getValueAt(i, 6);
                valorint = valorint + Integer.parseInt(valor);
            }
            etiqMonto.setText(valorint + " RD$");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

