    private void cambiarColoresDeEtiquetas(MouseEvent evt) {

        JLabel etiquetaSeleccionada = (JLabel) evt.getSource();
        String nombreDeBotonSeleccionada = etiquetaSeleccionada.getText();

        if (nombreDeBotonSeleccionada.equals("Estacionados")) {

            btnRetirados.setBackground(new Color(102, 102, 255));//blanco
            btnEstacionados.setBackground(new Color(42, 42, 167));//negro
            btnVentas.setBackground(new Color(102, 102, 255));

        } else if (nombreDeBotonSeleccionada.equals("Retirados")) {

            btnEstacionados.setBackground(new Color(102, 102, 255));
            btnRetirados.setBackground(new Color(42, 42, 167));
            btnVentas.setBackground(new Color(102, 102, 255));
        } else {
            btnVentas.setBackground(new Color(42, 42, 167));
            btnRetirados.setBackground(new Color(102, 102, 255));
            btnEstacionados.setBackground(new Color(102, 102, 255));
        }

        botonSeleccionado = nombreDeBotonSeleccionada;

        //De seguro usted leera esto, no pude cambiar el color del foreground del dateChooser si usted sabe como hacerlo
        //por favor envieme el codigo
    }

