    private void personalizacionDateChooser() {

        ArrayList<JDateChooser> listaDate = new ArrayList();
        listaDate.add(jDateChooser1);
        listaDate.add(jDateChooser2);

        for (JDateChooser jDateChooser : listaDate) {

            jDateChooser.setLocale(new Locale("ES"));
            jDateChooser.setDateFormatString("dd/MM/yyyy");

            jDateChooser.getCalendarButton().setContentAreaFilled(false);
            jDateChooser.getCalendarButton().setIcon(new ImageIcon("Imagenes/iconoCalendario.png"));

            JTextFieldDateEditor f = (JTextFieldDateEditor) jDateChooser.getDateEditor();

            f.setBackground(new Color(102, 102, 255));
            f.setBorder(null);
            f.setEditable(false);
        }

    }

