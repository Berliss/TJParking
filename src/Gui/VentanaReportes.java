    private void obtenerRegistrosParaLaTabla(String bdDeseada, int fechaDesde, int fechaHasta) {

        ArrayList<String> listaTodosLosDatos = bsd.obtenerTodosLosDatos(bdDeseada);
        ArrayList<String> listaDatosEspecifico;

        String dueno = "";
        String placa = "";
        String marca = "";
        String modelo = "";
        String color = "";
        String tipo = "";
        String fecha = "";
        String fechaEntrada = "";
        String monto = "";
        String estacionamiento = "";

        for (int i = 0; i < listaTodosLosDatos.size(); i++) {

            listaDatosEspecifico = bsd.obtenerDatosEnLineaEspecifica(listaTodosLosDatos.get(i));

            if (bdDeseada.equals("bdEntradas.txt")) {

                estacionamiento = listaDatosEspecifico.get(0);
                dueno = listaDatosEspecifico.get(1);
                placa = listaDatosEspecifico.get(2);
                marca = listaDatosEspecifico.get(3);
                modelo = listaDatosEspecifico.get(4);
                color = listaDatosEspecifico.get(5);
                tipo = listaDatosEspecifico.get(6);
                fecha = listaDatosEspecifico.get(7);
            } else if (bdDeseada.equals("bdSalidas.txt")) {

                dueno = listaDatosEspecifico.get(0);
                placa = listaDatosEspecifico.get(1);
                fechaEntrada = listaDatosEspecifico.get(2);
                fecha = listaDatosEspecifico.get(3);
                monto = listaDatosEspecifico.get(4);
                tipo = listaDatosEspecifico.get(5);
            }

            int fechaInt = Integer.parseInt(fecha.replaceAll(Pattern.quote("/"), ""));
            System.out.println("" + fechaInt);

            if (fechaInt >= fechaDesde && fechaInt <= fechaHasta) {

                if (bdDeseada.equals("bdEntradas.txt")) {

                    String[] datos = {estacionamiento, dueno, placa, marca, modelo, color, tipo, fecha};
                    modeloEstacionados.addRow(datos);
                } else if (bdDeseada.equals("bdSalidas.txt")) {
                    long Dias = metodosGenerales.DiasEntreFecha(fechaEntrada, fecha);
                    if (Dias == 0) {
                        Dias = 1;
                    }
                    String[] datos = {dueno, placa, tipo, fechaEntrada, fecha, new String("" + Dias), monto};
                    modeloRetirados.addRow(datos);
                }
            }
        }
    }

    private void borrarTabla(String bd) {

        if (bd.equals("bdEntradas.txt")) {

            int numeroDeFilas = modeloEstacionados.getRowCount();

            for (int i = numeroDeFilas - 1; i >= 0; i--) {
                modeloEstacionados.removeRow(i);
            }

        } else if (bd.equals("bdSalidas.txt")) {

            int numeroDeFilas2 = modeloRetirados.getRowCount();

            for (int i = numeroDeFilas2 - 1; i >= 0; i--) {
                modeloRetirados.removeRow(i);
            }

        } else {

            int numeroDeFilas3 = modeloVentas.getRowCount();

            for (int i = numeroDeFilas3 - 1; i >= 0; i--) {
                modeloVentas.removeRow(i);
            }
        }
    }
