    public void cargarTituloModelo(String[] titulos) {
        for (int i = 0; i < titulos.length; i++) {
            modelo.addColumn(titulos[i]);

        }
    }

    public void cargarFilasModelo(String bdDeseada) {

        ArrayList<String> todosUsuario = baseDatos.obtenerTodosLosDatos(bdDeseada);
        ArrayList<String> usuarioEspecifico;
        String[] datos;

        for (int i = 0; i < todosUsuario.size(); i++) {
            usuarioEspecifico = baseDatos.obtenerDatosEnLineaEspecifica(todosUsuario.get(i));
            datos = new String[usuarioEspecifico.size()];
            for (int j = 0; j < usuarioEspecifico.size(); j++) {
                datos[j] = usuarioEspecifico.get(j);
            }

            modelo.addRow(datos);

        }

    }

