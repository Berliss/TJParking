    private void pasarDatosDeTabla() {
        int numeroFilaSeleccionada = tabla.getSelectedRow();
        int numeroDeColumnas = modelo.getColumnCount();
        String linea = "";

        for (int i = 0; i < numeroDeColumnas; i++) {
            linea += modelo.getValueAt(numeroFilaSeleccionada, i) + "|";
        }

        switch (moduloQueMeInvoca) {

            case "VentanaClientes":
                VentanaClientes.setLineaProvinienteDeTabla(linea);
                VentanaClientes.rellenarCamposConDatosDeTabla();
                this.dispose();
                break;

            case "VentanaClientesDeVehiculos":

                VentanaVehiculos.setDuenoProvinienteDeTabla(linea);
                VentanaVehiculos.rellenarCampoDueno();
                this.dispose();
                break;

            case "VentanaVehiculos":

                VentanaVehiculos.setLineaProvinienteDeTabla(linea);
                VentanaVehiculos.rellenarCamposConDatosDeTabla();
                this.dispose();
                break;

            case "VentanaEntradaVehiculos":

                VentanaEntradaVehiculos.setLineaProvinienteDeTabla(linea);
                VentanaEntradaVehiculos.rellenarCamposConDatosDeTabla();
                System.out.println("Me ejecute");
                this.dispose();

        }
    }

